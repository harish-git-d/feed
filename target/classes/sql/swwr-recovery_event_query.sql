-- SWWR Recovery Event Query
SELECT
    'E' as RECORD_TYPE,
    TO_CHAR(SYSDATE, 'YYYY-MM-DD HH24:MI:SS') as EVENT_TIMESTAMP,
    'SWWR_RECOVERY_ADJUSTMENT' as EVENT_TYPE,
    sr.RECOVERY_ID as EVENT_ID,
    'SCEF' as SOURCE_SYSTEM,
    sr.RECOVERY_STATUS as EVENT_STATUS
FROM SWWR_RECOVERY sr
WHERE sr.COB_DATE = TO_DATE(:cobDate, 'YYYY-MM-DD')
ORDER BY sr.RECOVERY_ID